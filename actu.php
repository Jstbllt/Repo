	<h1>Une nouvelle étiquette énergie pour 2021</h1>

<p>Depuis plus de 20 ans, l’étiquette énergie aide les particuliers dans leurs achats d’appareils électroménagers. En 2021, cette étiquette emblématique va changer pour plus de clarté, supprimant la surenchère des signes « +++ ». </p>

	<h2>Ce qui change en 2021 pour l’étiquette énergie</h2>

	<p>Dès 2021, l’étiquette énergie obligatoire sur les appareils électroménagers indiquera une note allant de A à G, A étant le plus économe (en vert) et G étant le plus énergivore (en rouge). Au fur et à mesure de l’évolution des produits, les classes les moins économes seront progressivement grisées, signifiant que les appareils concernés n’existent quasiment plus. En effet, l’étiquette énergie n’est pas vouée à rester immuable : tous les 10 ans, une classe attribuée sera réévaluée afin de favoriser le progrès technologique et énergétique. Les fabricants devront alors redoubler d’effort pour proposer des produits bien classés sur l’échelle de A à G. Pour prendre un exemple, un appareil actuellement étiqueté A+++ aujourd’hui équivaudra à un C à partir de 2021. Enfin, la Commission Européenne a demandé aux fabricants d’électroménager de constamment mettre à jour une base de données gigantesque à l’échelle européenne qui permettra de mieux comparer tous les équipements vendus dans l'UE. Cette base de données, libre d’accès, est en cours de création et devrait être disponible courant 2021.</p>

	<h2>Entre 2021 et 2025, des priorités concernant les appareils électroménagers</h2>

	<p>L’entrée en vigueur de cette nouvelle étiquette énergétique se fera en plusieurs étapes pour laisser un peu de temps aux fournisseurs et aux magasins. Ainsi, les premiers appareils électroménagers à porter la nouvelle étiquette énergie à partir de mars 2021 seront : les machines à laver, les lave-vaisselle, les téléviseurs, les réfrigérateurs, les lampes et luminaires, les machines lavantes-séchantes et les congélateurs. D’autres appareils portant l’étiquette énergie auront, eux, jusqu’à 2025 pour se mettre aux normes. Il s’agit notamment	des fours, des climatiseurs, des sèche-linge, des aspirateurs, des hottes, ou encore des appareils de froid spécialisés. Il faudra attendre au moins 2030 pour voir les nouvelles étiquettes énergie sur les chauffe-eau et appareils de chauffage car ces derniers n’ont obtenu leur étiquette qu’en 2015 et sont donc encore d’actualité. </p>

	<h2>L’étiquette énergie toujours plus exigeante</h2>

	<p>Dans le but de protéger au mieux le consommateur mais également de réduire la consommation énergétique ménagère au quotidien, l’étiquette énergie se veut toujours plus intransigeante, forçant ainsi les fabricants à faire évoluer le marché et baisser les prix à terme. Les nouvelles classes A (voire A et B) seront intransigeantes et ne correspondront à aucun produit au départ, de façon à stimuler le progrès technologique et à laisser une place sur l'étiquette pour classer les produits du futur qui seront davantage économes que ceux d'aujourd'hui. Pour obtenir des produits d’électroménager toujours plus performants sur le marché, la Commission européenne a fixé des barèmes qui forceront une révision rapide des étiquettes énergétiques : lorsque 30 % des appareils du marché seront classés A ou lorsque 50 % des appareils seront classés A ou B, l’étiquette sera revue… à la hausse bien sûr !</p>

