		<h3>AquaSensor</h3>

		AquaSensor est un système mis au point par le groupe Bosch qui mesure la turbidité de l'eau pour optimiser la température et la consommation d'eau pendant tout le cycle de lavage en fonction du degré de salissure de la vaisselle. Il peut faire économiser jusqu’à 25% d’eau.

		<h3>AquaStop</h3>

		L’AquaStop est un dispositif de protection qui coupe automatiquement l'arrivée d'eau d’un lave-vaisselle ou d’un lave-linge en cas de fuite ou de débordement.

		<h3>Classe Climatique</h3>

		La classe climatique indique la plage de températures (extérieures) qui convient au bon fonctionnement d’un réfrigérateur ou d’un congélateur. Il existe 4 classes climatiques de base : <br>

		<ul>
		<li>N : tempérée (de +16 à +32°C)</li>
		<li>SN : tempérée élargie (de +10 à +32°C)</li>
		<li>ST : subtropicale (de +18 à +38°C)</li>
		<li>T : tropicale (de +18 à +43°C)</li>
		</ul>

		Auxquelles s’ajoutent 4 classes correspondant à des combinaisons des classes de base : <br>

		<ul>
		<li>SN-ST : subtropicale élargie (de +10 à +38°C)</li>
		<li>SN-T : tropicale élargie (de +10 à +43°C)</li>
		<li>N-ST : tempérée à tropicale (de +16 à +38°C)</li>
		<li>N-T : tempérée à tropicale (de+16 à +43°C)</li>
		</ul>

		Ces données seront d’autant plus déterminantes dans le choix de votre appareil que celui-ci sera placé dans un sous-sol, une cave, un garage, une dépendance ou une verrière mal isolés.

		<h3>CookAssist</h3>

		Cette fonction présente sur certains fours propose de nombreuses recettes préprogrammées. Il suffit de choisir un plat et le four sélectionne automatiquement la température, le mode de cuisson et la durée adaptés. 

		<h3>DLNA</h3>

		Digital Living Network Alliance = Alliance pour un réseau digital domestique.<br>
		Le DLNA est un standard qui permet de connecter plusieurs appareils et équipements multimédia au sein d'un même réseau et ce, indépendamment de leur marque. Ainsi, pourvu qu'ils soient certifiés DLNA, un téléviseur Panasonic est 100 % compatible avec un lecteur de Blu-ray Samsung, avec un ordinateur HP, un téléphone LG et ainsi de suite. Cette compatibilité certifiée peut s'appliquer à tous les équipements électroniques. 

		<h3>Dureté de l’eau</h3>

		Elle s'exprime en degrés français (°f) et indique la quantité de calcium et de magnésium contenus dans l'eau. L'eau dure (à partir de 30 °f) chauffée est à l'origine du dépôt de calcaire, appelé aussi tartre. Plus dure est l'eau, plus importante est la quantité de lessive nécessaire. Renseignez-vous en appelant votre distributeur d’eau ou en achetant des bandelettes de test. Certaines régions, comme la Bretagne ou les Vosges, ont une eau très douce qui rend inutiles les produits antitartre, sauf exception.

		<h3>Fonction Sabbat</h3>

		Le four chauffe en mode convection naturelle jusqu’à 74 heures avec une température réglable entre 85°C et 140°C, sans devoir toucher le bandeau une fois lancé. 

		<h3>Froid Brassé (ou dynamique)</h3>

		Le froid brassé (ou dynamique) est un procédé permettant le brassage de l’air froid grâce à un ventilateur situé sur la paroi arrière du frigo. Il se met en route dès qu’il y a un changement de température (souvent après qu’on a ouvert la porte). Cela permet une température plus homogène dans l’appareil et une suppression de la condensation sur les clayettes.<br>
		Cette bonne répartition du froid impacte directement la durée de conservation des aliments : deux à trois fois plus élevée que dans un frigo à froid statique. Ce type de froid vous permet de ranger les aliments où vous le souhaitez dans le réfrigérateur, sans qu’ils soient asséchés (déshumidifiés).<br>
		Comparé à un frigo froid statique, il y aura une redescente en température deux fois plus rapide après la fermeture du frigo. Comparé à un frigo à froid ventilé, la température ne sera pas totalement homogène.<br>
		Le froid brassé peut engendrer un mélange d’odeurs entre les aliments. Pensez donc à bien protéger vos aliments avant de les placer dans le réfrigérateur.<br>
		Le réfrigérateur à froid brassé fait du bruit au moment où le moteur se met en marche (quand l'appareil a détecté que la température intérieure a augmenté).<br>
		Le froid brassé est un excellent compromis entre le froid statique et le froid ventilé.

		<h3>Froid Statique</h3>

		Le froid statique est la technologie la plus ancienne pour les réfrigérateurs. Le principe est simple : l’air froid circule librement dans le réfrigérateur. Il n’y a pas de ventilation.
		L’air froid étant plus lourd, il va rester plutôt en bas du réfrigérateur alors que l’air chaud, plus léger, aura tendance à monter. La température n’est donc pas homogène dans le frigo.
		La température n’étant pas identique partout, cela implique la nécessité de bien ranger les différents types d’aliments selon les zones du réfrigérateur, afin d’optimiser leur conservation. Les denrées les plus fragiles seront donc entreposées en bas, au dessus du bac à légumes. Et plus le frigo est grand, plus la différence de température entre les zones sera importante.
		Le froid statique est la technologie la plus économe en électricité et la plus silencieuse (bien que de ce côté, les autres technologies fassent de plus en plus d’effort). Par contre, le réfrigérateur nécessite un dégivrage manuel régulier.
		Le réfrigérateur à froid statique est aussi le type de frigo le moins cher.
		Nos conseils : limitez les ouvertures, car le retour à la température optimale est lent ; et ne collez pas trop les aliments les uns aux autres !

		<h3>Froid Ventilé (ou No Frost)</h3>

		Le froid ventilé (ou no frost) est une technologie plus perfectionnée et plus haut de gamme.
		Le total No Frost a été inventé par les Coréens. Pour pallier aux problèmes du froid statique, Samsung et LG sont arrivés en force sur le marché européen avec des frigos équipés de cette technologie.
		Le principe : en plus du ventilateur qui répartit le froid de manière homogène, le réfrigérateur injecte de l’air froid et sec qui permet de refroidir plus rapidement et sans humidité. Résultat, l’air est débarrassé de son trop plein d’humidité, la diffusion de froid est constante et homogène. Et il n’y a pas de formation de givre dans l'appareil. Un gain de temps appréciable ! Cela évite également la prolifération bactérienne.<br><br>
		Les aliments sont donc refroidis uniformément dans l'ensemble du réfrigérateur. La conservation est 2 à 3 fois plus élevée que celle constatée dans un réfrigérateur à froid statique.
		Par contre, l’air injecté étant sec, il est vivement conseillé de recouvrir les aliments délicats ou de les mettre dans des boîtes afin d’éviter leur desséchement. Vous augmenterez ainsi leur durée de conservation. Faites surtout attention aux fruits et légumes ! Pour pallier au dessèchement des aliments, vous pouvez privilégier des frigos avec bac fraîcheur intégrant le réglage de l’humidité. Ces bacs fraîcheur sont de plus en plus proposés par les fabricants, et plébiscités par les consommateurs. Certains frigos possèdent deux tiroirs : un pour les légumes et un pour la viande. Ainsi, vos aliments sont conservés de manière optimale.
		Après la fermeture du frigo, la redescente en température est plus rapide qu’avec les deux autres technologies. Côté congélateur, plus besoin de dégivrage, les sacs ne collent plus entre eux, les portes et tiroirs s'ouvrent et se ferment sans se bloquer et les étiquettes restent lisibles.<br><br>
		Le réfrigérateur no frost fait un léger bruit au moment où le moteur se met en marche, quand l'appareil a détecté que la température intérieure a augmenté. Mais en règle générale, les réfrigérateurs sont de plus en plus silencieux.
		NB : SmartFrost, LowFrost, StopFrost, … Ce sont toutes des technologies de congélation qui sont des alternatives au froid statique de base et au froid ventilé, mais ce ne sont pas des « No Frost ». Le congélateur fonctionne ici de la même manière que le froid statique mais garantit 80% de givre en moins. Cela rend donc la corvée de dégivrage beaucoup plus simple, rapide et espacée dans le temps. La consommation d’énergie est également limitée. De plus, le congélateur n’ayant pas de ventilation, est plus silencieux.<br>
		Au sein de la famille des réfrigérateurs combinés No Frost, il existe deux variantes :<br>
		<ul>
		<li>Combi à froid ventilé total : réfrigérateur et congélateur sont équipés de ce système</li>
		<li>Combi à froid ventilé partiel : seulement le congélateur est No Frost, le réfrigérateur est à froid brassé.</li>
		</ul>
		
		Le réfrigérateur brassé + congélateur No Frost est la tendance du moment. Si vous êtes consommateur régulier d’aliments frais, il est conseillé d’opter pour ce système de froid ventilé partiel. Ainsi, vous pourrez bénéficier d’une humidité suffisante dans le réfrigérateur pour une conservation optimale des produits frais.

		<h3>Hotte évacuation</h3>

		C’est un système de rejet de l’air de la hotte à l’extérieur de la maison. Le raccordement de la hotte à un conduit relié à une cheminée ou une bouche spécifique est nécessaire. Ainsi, les vapeurs, fumées et odeurs sont directement évacuées à l’extérieur. Cela assure une efficacité d’élimination de 90 %. 

		<h3>Hotte recyclage</h3>

		Le recyclage est un système qui permet de nettoyer l’air aspiré en le débarrassant des particules graisseuses et odeurs à l’aide de filtres à charbon actifs avant de le rejeter dans la pièce. Ce système est employé lorsqu’il n’y a pas de système d’évacuation vers l’extérieur et assure une efficacité d’élimination d’environ 60 %. Les filtres des hottes à recyclage doivent être changés régulièrement pour assurer une pleine efficacité de la hotte.

		<h3>Induction ou vitrocéramique ?</h3>

		On a tendance à confondre l’induction et la vitrocéramique. Pourtant, ces termes désignent deux aspects très différents d’une table de cuisson : l’induction est une technologie de cuisson (comme le sont également l’halogène, le gaz ou l’électrique). La vitrocéramique est la matière dans laquelle est fabriqué le verre de la table de cuisson. 

		<h3>Intégrable ou encastrable ?</h3>

		La définition d’un appareil électroménager intégrable est en quelque sorte un abus de langage. En effet, un élément intégrable désigne tous les appareils qui ne sont pas en « pose libre ». Ces derniers correspondent aux appareils électroménagers que l’on peut mettre n’importe où, là où l’utilisateur souhaite les installer. Par opposition à la « pose libre », on trouve donc les appareils intégrables, qui visent à être installés dans des niches ou des coffres. Un appareil  intégrable est un appareil qui est livré dépourvu de façade, de parois latérales et de plateau (appelé le « top ») afin de l’intégrer facilement dans l’environnement d’une cuisine équipée. L’utilisateur applique sur la porte une façade adaptée aux couleurs des meubles pour une intégration totale.
		<br>
		A l’intérieur de la catégorie des « intégrables », on trouve 3 catégories :<br>
		<ul>
		<li>les semi-intégrables : appareils avec façade reprenant les coloris des placards (non fournie), mais bandeau de commande apparent. La machine reste alors clairement identifiable dans la cuisine par la présence des voyants, des boutons, des écrans lumineux et de la poignée d’ouverture.</li>  
		<li>les tout intégrables (ou full intégrables) : appareils sans bandeau de commande apparent. La machine est alors totalement dissimilée derrière la façade reprenant les coloris des placards. Cette option rend la cuisine aménagée la plus harmonieuse possible. Le tableau de bord du lave-vaisselle tout intégrable se trouve sur la tranche supérieure de la porte. Comme il est caché à la vue, certains modèles disposent d’un témoin lumineux de fonctionnement dont la lumière est projetée sur le sol. L’affichage indique aussi le temps restant. Cela permet de savoir si le lave-vaisselle a terminé ou non son programme de lavage.</li>
		<li>les encastrables : appareils hybrides livrés sans plateau supérieur (top) mais avec façade non habillable. Ils sont prêts à être encastrés sous un plan de travail mais ne sont pas dissimulables derrière une façade reprenant les coloris des placards. En revanche, ils ont le décroché en bas de l’appareil permettant l’installation d’une plinthe de décoration. Ces appareils sont très rares et correspondent à un besoin d’esthétique très spécifique.</li>
		</ul>
		<br>
		Il existe aussi des appareils « pose libre » dont on peut retirer le plateau supérieur (le top) pour pouvoir les glisser sous un plan de travail. C’est le cas de la majorité des lave-vaisselle, mais de quelques modèles de lave-linge seulement. Mais ces appareils n’étant pas « intégrables » ne peuvent pas être habillés par un panneau de décor.

		<h3>Nettoyage Catalyse</h3>

		Dans le cas du nettoyage par catalyse, les parois de l’appareil, dites catalytiques, sont composées d’un revêtement spécial qui va absorber les graisses dès que la température dépasse les 200 ou 220°C. Ainsi, pendant que vous cuisinez, le four va se nettoyer au fur et à mesure. Dès lors aucune dépense énergétique supplémentaire n’est nécessaire.
		<br>
		Souvent moins chers que les fours à pyrolyse, les fours catalytiques nécessitent néanmoins un entretien manuel pour la paroi vitrée ou le fond qui ne sont pas catalytiques. A noter également que ces parois doivent être changées environ tous les cinq ans pour rester efficaces et ne doivent pas être nettoyées avec de l’eau ou du détergent, mais juste avec une brosse douce.
		<br>
		L’émail autodégraissant est surtout adapté aux corps gras, mais pas au sucre. Attention, le sucre a tendance à obstruer les micropores des parois catalytiques. Donc en cas d’éclaboussures de sucre, nettoyez à l’eau rapidement les résidus encore tièdes. De ce fait, le four catalyse conviendra mieux à ceux qui cuisinent peu de pâtisseries.

		<h3>Nettoyage Ecoclean</h3>

		La technologie Ecoclean est un peu à mi-chemin entre la pyrolyse et la catalyse. Il est plus économique que la pyrolyse et plus efficace que la catalyse. Le four est équipé de parois composées d’un revêtement spécial : une céramique microporeuse (plus efficace que les parois catalytiques) qui absorbe et retient de l’oxygène. Durant la cuisson, les graisses sont absorbées puis, au cours du nettoyage, l’oxygène va les oxyder pour les détruire. Un système très efficace, à condition que les projections de graisse ne soient pas trop importantes. Pour le nettoyage, une heure de chauffe à 270°C suffit à éliminer les graisses (contrairement au four à catalyse, les parois sont remplies d’oxygène et les graisses sont complètement détruites). Quand le four est refroidi, il suffit de passer une éponge humide pour récupérer l’ensemble des saletés.
		Ce système requiert moins d’énergie que la pyrolyse et est par conséquent plus économique. Par rapport aux fours à catalyse, l’avantage est que les parois ne seront pas à changer.

		<h3>Nettoyage Hydrolyse</h3>

		Economique et écologique, le système à hydrolyse est un procédé nouveau et simple qui nettoie votre four grâce à un bain de vapeur. Pour nettoyer le four, il vous suffit de verser un peu d’eau (0,40 à 0,60 litres) mélangée à quelques gouttes de liquide vaisselle sur la sole du four hydrolyse (paroi inférieure) puis de programmer le four sur le mode hydrolyse. Une fois le programme lancé, le four chauffe pendant quatre à cinq minutes pour que l’eau se transforme en vapeur qui permet de ramollir et de décoller les graisses. Le programme dure généralement 30 minutes. A la fin du cycle de nettoyage, un coup d’éponge dans la cavité du four suffit.

		<h3>Nettoyage Manuel</h3>

		Le nettoyage manuel – comme son nom l’indique – demande un entretien de votre four à la main et ce, de manière régulière. A l’aide d’une éponge non abrasive imbibée de détergent doux (liquide vaisselle par exemple), vous devrez ramasser les éclaboussures et autres salissures tant que le four est tiède afin de les enlever plus facilement mais aussi afin de ne pas les recuire lors d’une future cuisson, ce qui rendrait leur nettoyage beaucoup plus complexe.
		Les fours traditionnels sont généralement recouverts d’un revêtement émaillé et anti-adhésif qui s’appelle l’émail lisse. En plus d’un nettoyage régulier, vous devrez nettoyer votre four à l’aide d’un produit dédié tous les trois mois. Attention cependant à manipuler ces produits avec précaution car ils contiennent des ingrédients corrosifs. Si vous êtes à la recherche d’une solution plus écologique, un mélange d’eau et de bicarbonate de soude – formant une pâte – peut également faire l’affaire. A laisser agir dans votre four durant une nuit, vous n’aurez ensuite besoin que d’une éponge pour vous débarrasser des résidus.

		<h3>Nettoyage Pyrolyse</h3>

		Le nettoyage par pyrolyse est particulièrement efficace. Lorsque vous enclenchez la fonction pyrolyse, le four se met à chauffer à très haute température pour atteindre près de 500°C. Avec une telle chaleur, graisses et débris organiques sont littéralement réduits en cendres et il ne reste plus (après refroidissement complet de l’appareil) qu’à essuyer celles-ci avec une éponge ou un chiffon humide. En revanche, cette méthode prend du temps puisqu’il faut compter entre une et deux heures pour un résultat optimal. De tels fours sont le plus souvent équipés d’une porte froide et d’un système de verrouillage automatique afin d’éviter tous risques de brûlures, notamment pour les enfants. 

		<h3>Sèche-linge à condensation</h3>

		Le sèche-linge à condensation fonctionne grâce à une résistance chauffante et en circuit fermé.
		La vapeur est refroidie en eau, puis évacuée dans un bac récupérateur.
		Ce système nécessite de nettoyer régulièrement le condenseur et les filtres et de vider régulièrement le bac récupérateur d'eau.

		<h3>Sèche-linge à évacuation</h3>

		Le sèche-linge à évacuation fonctionne grâce à une résistance chauffante, l’appareil va rejeter de l'air chaud, saturé en humidité à l’extérieur, et nécessite donc la présence d’une gaine d’évacuation.
		Celle-ci doit mesurer au moins 1m20 de longueur et être située à 30 cm du sol. 

		<h3>Sèche-linge à pompe à chaleur</h3>

		Le système de pompe à chaleur n'utilise pas de résistance et permet donc une économie d’énergie importante, d’où une baisse importante de la consommation d’électricité (jusqu’à -60% par rapport à un sèche-linge classique de classe A). Comme un sèche-linge à condensation, la vapeur est refroidie en eau, puis évacuée dans un bac récupérateur.
		Ce système nécessite donc également de vider le bac récupérateur d'eau, et nettoyer les filtre. 

		<h3>Traces blanches sur la vaisselle</h3>

		Il existe trois principales sortes de traces blanches (ou dépôts blanchâtres) sur les verres : <br>
		<ul>
		<li>Les traces sont en surface uniquement et s’en vont facilement : dans ce cas, il peut s’agir soit d’un excès de produit de lavage (trop de poudre ou tablette surdosée), soit du sel se retrouve dans votre lave-vaisselle (compartiment du sel mal fermé, non étanche …). La vaisselle aura dans ce cas, un goût salé.</li>
		<li>Les traces sont cette fois-ci incrustées, mais s’en vont grâce à un produit anti-calcaire : dans ce cas, vérifiez si l’adoucisseur de votre lave-vaisselle fonctionne bien, et si votre réservoir de sel est bien rempli.</li>
		<li>Les traces sont incrustées avec des rayures et elles ne partent pas : deux possibilités : la température de l’eau est trop élevée (baissez là), votre poudre ou tablette de lavage est trop alcaline et abîme votre vaisselle en la nettoyant « trop ». Dans ce cas, optez pour un autre produit moins agressif, faites des tests, et diminuez éventuellement la quantité de produit.</li>
		</ul>
		<br>
		A noter : les traces incrustées dans le verre ne sont pas nettoyables ni récupérables.

		


